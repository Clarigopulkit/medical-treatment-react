export const Regex = {
  NAME: /^([a-zA-Z]+\s)*[a-zA-Z]+$/,
  COUNTRY: /[a-zA-Z]{2,}/,
  ZIP: /(^\d{5}$)|(^\d{5}-\d{4}$)/,
  Year: /(^\d{4}$)|(^\d{4}-\d{4}$)/,
  PHONE: /^[0-9]{3}([ -]?)[0-9]{3}([ -]?)[0-9]{4}$/,
  Registeration_number: /^[a-zA-Z0-9_/-]*$/,

  EMAIL_OR_PHONE: /^(?:\d{10}|\w+@\w+\.\w{2,3})$/,
  SPECIAL_CHARACTERS: /~`!@#$%^&*()-+={}\\[\\]\\|:;\"<>,./,
  EMAIL:
    /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  PASSWORD:
    /^.*(?=.{8,})(?=.*\d)((?=.*[a-z]){1})((?=.*[A-Z]){1}).*$/,
  PASSWORD_2: /(?=.*\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[\W]).{6,20}/,
};



